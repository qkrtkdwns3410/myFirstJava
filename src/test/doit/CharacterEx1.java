public class CharacterEx1 {
      public static void main(String[] args) {
            char ch1 = 'A';
            System.out.println("ch1 = " + ch1); //해당 문자의 출력
            System.out.println("ch1 = " + (int) ch1); //문자에 해당하는 정수 값 (아스키 코드 값) 출력

            System.out.println("========================");

            char ch2 = 66; //정수 값의 대입
            System.out.println("ch2 = " + ch2); //  정수값에 해당하는 문자 출력

            System.out.println("========================");

            int ch3 = 67;
            System.out.println("ch3 = " + ch3); //문자 정수값의 출력
            System.out.println("ch3 = " + (char)ch3); //정수 값에 해당하는 문자의 출력
      }
}

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    